export const ID_PATTERN = /^20\d{8}$/;

// (?=.*[A-Z])
// 최소 1개 이상의 **대문자(A~Z)**를 포함해야 함

// (?=.*[a-z])
// 최소 1개 이상의 **소문자(a~z)**를 포함해야 함

// (?=.*\d)
// 최소 1개 이상의 **숫자(0~9)**를 포함해야 함

// (?=.*[^A-Za-z0-9])
// 최소 1개 이상의 특수문자가 포함되어야 함
// 단, 여기서 “특수문자”란 알파벳과 숫자를 제외한 모든 문자이므로, 스페이스나 한글 등도 포함될 수 있습니다.
// 만약 특정 특수문자(예: @#$%&*?)만 허용하고 싶다면, (?=.*[@#$%&*?])처럼 수정 가능합니다.
// .{9,}$

// 전체 문자열 길이가 9자 이상
// 최대 길이를 제한하고 싶다면, 예: .{9,20}$ (9~20자)

export const PASSWORD_PATTERN =
  /^(?=.*[A-Z])(?=.*[a-z])(?=.*\d)(?=.*[!@#$%&*?,.]).{9,20}$/;
